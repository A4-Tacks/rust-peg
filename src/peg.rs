#![feature(quote, box_syntax, core, collections, rustc_private, io, path, unicode, os, env, box_patterns)]
extern crate syntax;

use std::str;
use std::old_io::{stdin,stdout,stderr};
use std::old_io::fs::File;
use std::env;
use translate::{compile_grammar};

mod translate;
mod grammar;
mod rustast;
mod fake_extctxt;

fn print_usage(prog: &str) {
	println!("Usage: {} [file]", prog)
}

fn main() {
	let args = env::args_os().collect::<Vec<_>>();
	let progname = args[0].to_str().unwrap();

	let source_utf8 = match &args[1..] {
		[ref arg] if arg.to_str() == Some("-h") => return print_usage(progname),
		[ref fname] => File::open(&Path::new(fname.to_str().unwrap())).read_to_end().unwrap(),
		[] => stdin().read_to_end().unwrap(),
		_ => return print_usage(progname),
	};

	let source = str::from_utf8(source_utf8.as_slice()).unwrap();
	let grammar_def = grammar::grammar(source);

	match grammar_def {
		Ok(grammar) => {
			fake_extctxt::with_fake_extctxt(|e| {

				let ast = compile_grammar(e, &grammar);
				let mut out = stdout();

				out.write_line("// Generated by rust-peg. Do not edit.").unwrap();
				out.write_line("#![allow(non_snake_case, unused)]").unwrap();

				for item in ast.items.iter() {
					out.write_line(rustast::item_to_string(&**item).as_slice()).unwrap();
				}
			})
		}

		Err(msg) => {
			let mut e = stderr();
			(writeln!(&mut e, "Error parsing language specification: {}", msg)).unwrap();
			env::set_exit_status(1);
		}
	}
}
